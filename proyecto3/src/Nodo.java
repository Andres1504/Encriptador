
public class Nodo {
	
	public char letra;
	public Nodo siguiente;
	
	public Nodo(){

	}

	public char getDato() {
		return letra;
	}

	public void setDato(char letra) {
		this.letra = letra;
	}

	public Nodo getSiguiente() {
		return siguiente;
	}

	public void setSiguiente(Nodo siguiente) {
		this.siguiente = siguiente;
	}
	

}
